//Q1.Explore and explain the various methods in console function
//1.To write an error message to the console if assertion is false, we use:
console.assert();
//2.To clear the console:
console.clear();
//3.To Logs the number of times a particular call to count() has been called:
console.count();
//4.Outputs an error message to the console
console.error();
//5.Creates a new inline group until console.groupEnd() is called
console.group();
//6.Creates a new inline COLLAPSED group in the console
console.groupCollapsed();
//7.Exits the current inline group in the console
console.groupEnd();
//8.Outputs an informational message to the console
console.info();
//9.Outputs a message to the console
console.log();
//10.Displays tabular data as a table
console.table();
//11.Starts a timer (can track how long an operation takes)
console.time();
//12.Stops a timer that was previously started by console.time()
console.timeEnd();
//13.Outputs a stack trace to the console
console.trace();
//14.Outputs a warning message to the console
console.warn();