//Q3.brief intro on available data types in javascript

/* Javascript data types are dynamic

1. Strings : String type is used to represent textual data.
2. Numbers : The number type has only one integer with two representations: 0 is represented as both -0 and +0.
3. Booleans : Boolean represents a logical entity and can have two values: true and false
4. undefined : A variable that has not been assigned a value has the value undefined
5. Bigint : The BigInt type is a numeric primitive in JavaScript that can represent integers with arbitrary precision. With BigInts, you can safely store and operate on large integers even beyond the safe integer limit for Numbers.
6. Symbol : A Symbol is a unique and immutable primitive value and may be used as the key of an Object property
7. null : The Null type has exactly one value: null.
8. object : an object is a value in memory which is possibly referenced by an identifier.
*/